#include <stdio.h>

int main(void) {
  int x = 10;
  int y = 2;

  printf("Soma: %d\n", x + y);
  printf("Subtração: %d\n", x - y);
  printf("Multiplicação: %d\n", x * y);
  printf("Divisão: %d\n", x / y);
  printf("Módulo: %d\n", x % y);

  return 0;
}

